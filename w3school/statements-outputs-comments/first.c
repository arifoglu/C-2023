#include <stdio.h>

int main() {
  //// use \n  => pass to new line 
  printf("Hello World!!!\n");
  printf("Salut!! \n I m learning C \n and then will be C++ \n");

  // two \n will create a blank line
  printf("Bonjour \n\n");
  printf("Hallo \n");

  // \t  create horizontal tab
  printf("hi \t");
  printf("hi \n");

  // \\ create \ character
  printf("hi\\hi");
  printf("\n");

  // \"  create ""
  printf("hi I am a \"student\" \n");


  // comments
  printf("salut from me"); // this line is a comment 

  return 0;

}
