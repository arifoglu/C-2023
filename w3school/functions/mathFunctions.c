#include <stdio.h>
#include <math.h>

int main(){
    
    // square root   => sqrt()
    printf("%.f\n",sqrt(16));  // result 4
    
    //Round a number => ceil()
    printf("%.f\n",ceil(1.4));  // result 2

    //Round a number => floor()
    printf("%.f\n", floor(1.4)); // result 1

    //Power a number => pow()
    printf("%.f\n",pow(2,3));       // result 8
    
    //cube root 
    printf("%.f\n",cbrt(8));        // result 2

    return 0;
}